#include<stdio.h>
int main(){
    int a;
    printf("Enter a:");
    scanf("%d",&a);
    printf("a is %d\n",a);
    float b;
    printf("Enter b:");
    scanf("%f",&b);
    printf("b is %f\n",b);
    char ch;
    printf("Enter ch:");
    scanf("%c",&ch);
    printf("ch is %c",ch);
    return 0;
}